    public boolean addGradeButton(){
        String courseID = courseTxt.getText();
        String studentID = studentTxt.getText();
        int grade = Integer.parseInt (gradeTxt.getText());
        try{
        Connection con1 =DriverManager.getConnection("jdbc:derby://localhost:1527/LMS","bebo","bebo");
        Statement st = null;
        st = (Statement) con1.createStatement();
        st.executeUpdate("UPDATE APP.GRADES SET GRADE ="+grade+" WHERE STUDENTID_FK ='"+studentID+ "' AND COURSEID_FK ='"+courseID+"'");
        updateTable();
        Statement stat = (Statement) con1.createStatement();
        ResultSet rs = stat.executeQuery("SELECT GRADE FROM APP.GRADES");
        while(rs.next()){
        int grades = rs.getInt(1);
        
        return grades == grade;
        
        }
             
        
        }catch(SQLException ex){
            Logger.getLogger(LecturerGrade.class.getName()).log(Level.SEVERE, null, ex);
        }
     return false;
     
    }

