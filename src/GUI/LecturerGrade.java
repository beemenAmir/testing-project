    public boolean updateTable(){
     try{
        Connection con1 =DriverManager.getConnection("jdbc:derby://localhost:1527/LMS","bebo","bebo");
        Statement st = null;
        ResultSet rs = null;
        st = (Statement) con1.createStatement();
        rs = st.executeQuery("SELECT * FROM APP.GRADES");
        String columns[] = { "Student ID", "Course ID", "Grade" };
      String data[][] = new String[8][3];
    
      int i = 0;
      while (rs.next()) {
        String SID = rs.getString(1);
        String CID = rs.getString(2);
        String grade = Integer.toString(rs.getInt(3));
        data[i][0] = SID;
        data[i][1] = CID;
        data[i][2] = grade;
        i++;
      }
       DefaultTableModel model = new DefaultTableModel(data, columns);
       jTable1.setModel(model);
       jTable1.setShowGrid(true);
       jTable1.setShowVerticalLines(true);
       return jTable1.getRowCount() !=0;
        }catch (SQLException ex) {
        Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
       }
    return false;
    }
